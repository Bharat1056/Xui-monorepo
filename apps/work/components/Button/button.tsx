          "bg-secondary text-secondary-foreground shadow-xs hover:bg-secondary/80",
        ghost:
          "hover:bg-accent hover:text-accent-foreground dark:hover:bg-accent/50",
        link: "text-primary underline-offset-4 hover:underline",
        gradient:
          "relative group bg-zinc-950 text-white font-semibold rounded-full shadow-2xl shadow-zinc-900 focus-visible:ring-2 focus-visible:ring-offset-2 focus-visible:ring-sky-400 focus-visible:ring-offset-zinc-950 before:content-[''] before:pointer-events-none before:absolute before:inset-0 before:rounded-full before:bg-[image:radial-gradient(75%_100%_at_50%_0%,rgba(56,189,248,0.35)_0%,rgba(56,189,248,0)_75%)] before:opacity-0 group-hover:before:opacity-100 before:transition-opacity before:duration-500 before:-z-[1] after:content-[''] after:pointer-events-none after:absolute after:bottom-[0.125rem] after:left-[15%] after:w-[70%] after:h-px after:bg-gradient-to-r from-emerald-400/0 via-emerald-400/90 to-emerald-400/0 after:opacity-0 group-hover:after:opacity-40 after:transition-opacity after:duration-500",
